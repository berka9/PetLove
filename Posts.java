package com.example.pet1;

public class Posts {

    public String AnimalName, AnimalAge, AnimalGender, AnimalBreed,AnimalLocation,AnimalNumber,Animal;

    public Posts() {

    }

    public Posts(String AnimalName,String AnimalAge,String AnimalGender, String AnimalBreed,
                 String AnimalLocation, String AnimalNumber, String Animal){
        this.AnimalName = AnimalName;
        this.AnimalAge = AnimalAge;
        this.AnimalGender = AnimalGender;
        this.AnimalBreed = AnimalBreed;
        this.AnimalLocation = AnimalLocation;
        this.AnimalNumber = AnimalNumber;
        this.Animal = Animal;
    }
}
